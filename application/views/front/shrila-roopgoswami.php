                  <p><b>Shrila Rupa Goswami</b> appeared in 1489 in Karnataka, South India. He was the younger brother of Shrila Sanatan Goswami.</p>



<p>Forced by various circumstances, Shrila Rupa Goswami and Sanatan Goswami had to work for the Muslim government of Bengal under Nawab Hussein Shah. Rupa Goswami was then known by the Muslim name Dabir Khas ('private secretary'). Although he enjoyed great wealth and prestige, he never forgot Lord Shri Krishna. Even before meeting Shri Chaitanya Mahaprabhu, Rupa Goswami had already written several books on Vedic philosophy and was renowned for his learning and devotion.</p>







    <p>1. To uncover the lost sites of Lord Krishna's pastimes,</p>

 <p>2. To install Deities of the Lord and arrange for Their worship,</p>

    <p>3. To write books on Krishna consciousness, and</p>

    <p>4. To teach the rules of devotional life.</p>



<p>At first Rupa Goswami felt great difficulty carrying out the desire of Lord Chaitanya. But one day, while Rupa was sitting on the bank of the Yamuna River contemplating his mission, a beautiful boy came to him and asked the cause of his despondency. Rupa Goswami explained. The boy then led him to a small hill. “Inside this hill,” said the boy, “is the beautiful Deity Govind Dev.” He said that the Deity had been buried to protect Him during a Muslim invasion.</p>



<p>The next day Rupa Goswami led a group of villagers to the site and had them excavate the hill. The Deity Govind Dev was unearthed. Rupa Goswami then had a magnificent temple constructed under the patronage of Emperor Akbar and Maharaja Man Singh of Amber, Rajasthan. Shri Govind Dev is presently being worshipped at Jaipur, Rajasthan.</p>



<p>Rupa Goswami fulfilled all four parts of the mission given to him by Lord Chaitanya, including writing many books on Krishna consciousness. He passed away in 1564. Devotees pay respects to him by visiting his samadhi (tomb), in the courtyard of the Radha-Damodar temple in Vrindavan.</p>

