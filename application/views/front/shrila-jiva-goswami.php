                  <p><b>Jiva Goswami</b> was the son of Shri Vallabh and nephew of Shri Sanatan, Shri Rupa, all of whom were employed in the service of the Badsha Hussein Shah. Having been rewarded richly by the Badsha for their devices, their household life was very opulent.</p>



<p>When Shri Chaitanya Mahaprabhu came to Ramakeli, Sri Jiva was blessed by having darshan of his worshipable Lord, though he was just a baby at that time. Mahaprabhu indicated him to be a great future scholar in the Gaudiya sampradaya. Though he was only a child, Shri Jiva would meditate on Shri Chaitanya constantly.</p>



<p>Later on, when his father and uncles renounced their family life in order to be with Mahaprabhu, the only child, Shri Jiva, was left with his mother in the family palace at Fateyabad. Jiva missed them greatly and whenever he would remember his father and uncles, or Shri Chaitanya he would lose consciousness.</p>



<p>As he grew up, Sri Jiva took up the worship of the Deities of Shri-Shri Rama-Krishna. He would carefully decorate Them and offer bhog and arati, serving Them with his full attention. Even in his play, whatever games he played were connected with Shri Krishna's pastimes.</p>



<p>While studying under the local pandits he became proficient in grammar, poetry and rhetoric. His teachers noted his great intellect.</p>



<p>When Shri Jiva learned that his father had passed away on the banks of the Ganga, he was completely unsettled. Family members and friends tried to console him but to little avail. Family life had become the source of his utter sadness. Someone suggested that he go to Navadvip and see Lord Nityanand, so he set out for Navadvip with a group of pilgrims. Everyone at Navadvip was very happy to meet the nephew of Shri Rupa and Sanatan.</p>



 <p>Shri Jiva spent some days with Nityanand Prabhu, touring the nine islands of Navadvip, in order to visit the holy places of the Lord's pastimes there. Then, as ordered by Nityanand Prabhu, he set out for Kashi (Varanasi). At Kashi he studied Vedanta under Sri Madhusudan Vachaspati, a disciple of Sarvabhaum Bhattacharya. The conclusions of Vedanta contained in Shrimad-Bhagavatam that were expounded by Shri Chaitanya Mahaprabhu to Sarvabhauma Bhattacharya in Puri had been in turn taught by the Bhattacharya to Madhusudana Vachaspati, who established a school at Kashi.</p>



<p>From here Sri Jiva set out for Vrindavan where he received shelter at the lotus feet of his two uncles, Shri Rupa and Sanatana. Jiva stayed with Shri Rupa, who began to teach him Shrimad-Bhagavatam. After initiating him with the divine mantra, Rupa engaged him in the service of Shri Shri Radha-Damodar.</p>



 <p>Seeing that Jiva had quickly become conversant with the conclusion of Shrimad-Bhagavatam, Shri Rupa engaged him in proof-reading his Bhakti-rasamrita-sindhu. At this time Shri Jiva compiled a commentary on Bhakti-rasamrita-sindhu called Durgama-sangamani. Shri Sanatan Goswami compiled Shri Vaishnav-tosani, a commentary on the tenth canto of Shrimad-Bhagavatam, which he gave to Shri Jiva for proof-reading. Under the order of Shri Sanatana, Shri Jiva compiled a commentary on that named Laghu Vaishnav-tosani. His writings, along with those of Shri Rupa and Shri Sanatana, Shri Gopal Bhatt, Shri Raghunath Bhatt, Sri Raghunath Das, Shri Krishna Das, Shri Kashishvar Pandit, and Shri Madhu Pandit, completely captivated the learned men of that time.</p>

