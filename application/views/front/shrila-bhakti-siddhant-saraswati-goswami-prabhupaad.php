                  <p><b>Bhaktisiddhant Saraswati Thakura Prabhupaad</b> was born in 1873 at Jagannatha Puri. His father, Kedar Dutta Bhaktivinoda Thakura, was magistrate of the city and superintendent of Jagannatha temple. Once, during the Ratha yatra festival, the car stopped in front of the family's house. Taking the opportunity, the mother of Bhaktisiddhanta Sarasvati came out with the baby and put him on the Lotus Feet of Jagannatha. A garland from the hand of Jagannatha fell down on the baby.</p>

<p>Bhaktisiddhanta Sarasvati was educated in Calcutta and became learned in Mathematics and Astronomy. In his childhood he once took a mango which was meant as an offering for the Deity. When his father rebuked him, saying that he should not have taken the fruit which was meant for the Lord, the child took it very seriously and thought himself a great offender, promising not to take mango again throughout his whole life. He was known as Bimal Prasad Dutta before his renunciation. On account of his writing a book on astronomy known as Surya Siddhanta, he received the title Siddhanta Sarasvati. And, when he took sannyasa, preaching the gospel of Lord Chaitanya, he was known as Bhaktisiddhanta Sarasvati. He was acclaimed a great scholar in Calcutta, and the celebrated Dr. Kalidas Nag used to call him 'a living encyclopedia'.</p>



<p>Thakura Bhaktivinoda trained his son in childhood for preaching, and, after taking sannyasa, Bhaktisiddhanta Sarasvati started the Gaudiya Mission Institution. He started 64 branches of the Gaudiya Math all over India plus one in London and another in Berlin.

Bhaktisiddhanta Sarasvati Thakura was the spiritual master of Shrila Bhakti Dayit Madhav Goswami Ji Maharaj, the founder-acharya of Shri Chaitanya Gaudiya Math.</p>

