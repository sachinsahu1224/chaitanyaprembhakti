                <div class="Introduction">

                  <p><b>Srila Bhakti vinod Thakua</b>, also known as Kedarnatha Datta was born in 1838, in Birangara (Bengal ). The youngest of seven sons, his father Raja Krisananda Datta was a great devotee of Lord Nityananda. The family lived in fairly wealthy circumstances, until Kedarnatha's father passed away in 1849, when they were reduced to poverty. In 1850, Kedarnatha's mother arranged a marriage with the daughter of Madhusudana Mitra Mahashaya; a resident of Rana Ghata.</p>

<p>Kedarnatha was taught by his uncle Kashiprasada Ghosh Mahashaya Thakura, at his home in Calcutta. Kashiprasada, having studied under the British education system was a central figure in many literary circles and the editor of the Hindu Intelligencer. Kedarnatha studied his uncle's books and helped him in submitting articles to the newspaper. He became an expert in English as a reader, speaker and writer. When he was 18, he went to college in Calcutta, where he wrote extensively in both English and Bengali (these essays were published locally). He also lectured frequently in both languages. His best friend through these years was Sriman Dvijendranatha Thakura, the eldest son of Maharsi Devendranatha Thakura.</p>

<p>Having seen the corruption in the business world, Kedarnatha decided to become a teacher. He established an English school in Kendrapara, a village near Chutigrama in Orissa, thus pioneering English education in the state. Some time later he went to Puri and passed a teachers' examination; he got a teacher's post in a Cuttack school and later became the headmaster of a school in Bhadrak and then in Madinipura. His dedicated work was noted by the school-board authorities. As headmaster, Bhaktivinoda studied the various religions and came to the realization that the only real religion established in Bengal , albeit poorly, was that of Sri Chaitanya Mahaprabhu.</p>

<p>In 1861, Bhaktivinoda took up a government post as deputy magistrate in Bengal . In the years to come, he took up several government posts, chiefly as deputy magistrate in various parts of Bengal . He read Sri Chaitanya-Charitamrita repeatedly; his faith in Krishna consciousness increasing until he was totally absorbed in the pure bhakti-sastras day and night.</p>

<p>Between the years 1874 and 1893, Bhaktivinoda Thakura spent much time in seclusion, chanting the holy name (though he still executed his worldly duties); he wrote several books in Sanskrit such as Sri Krishna-samhita, Tattva-sutram and Tattva-viveka and Datta-kausubha. He spoke Bengali, Sanskrit, English, Latin, Urdu, Persian and Oriya. He managed to obtain and write a Sanskrit commentary on the Chaitanyopanishad.</p>

<p>Whilst in Puri, Srila Bhaktivinoda Thakura became manager of the Jagannatha Temple . He established regular worship of the Deity. In the temple courtyard he established a 'Bhakti Mandapa', where daily discourses of Srimad-Bhagavatam were held. Srila Bhaktivinoda Thakura would spend long hours discussing Krishna and chanting the holy name, especially at Tota-Gopinatha Mandir, the tomb of Haridasa Thakura, the Siddha Bakula and the Gambhira.</p>

<p>Bimala Prasad (Bhaktisiddhanta Sarasvati), Bhaktivinoda's fourth son, was born in Sri Purushottama Kshetra (Jagannatha Puri) on 6th February 1874 AD, answering the prayer of Bhaktivinoda for the Lord 'to send a Ray of Vishnu' to preach the message of Sri Chaitanya Mahaprabhu all over the world. He was given the name Bimal Prasad. Sri Srimad Bhaktisiddhanta Sarasvati Prabhupada was to become the spiritual master of Shrila Bhakti Dayit Madhav Goswami Ji Maharaj.f

As a devotee, Bhaktivinoda showed great humility; he had strict moral standards and would not accept gifts from anyone. Even in his government duties, he refused all honours and titles.

When his seventh son was born, Srila Bhaktivinoda Thakura took pancharatrika diksa initiation from Bipin Bihari Goswami, descended from the Jahnava family of Baghnapara. In 1881 Bhaktivinoda Thakura began publishing Sajjanatosani, his Vaishnava journal.

Srila Bhaktivinoda Thakura had previously made pilgrimages to Kashi, Prayaga, Mathura and Vrindavana in 1866. At the close of his stay in Naraila he desired to again see the land of Vraja , where he met Srila Jagannatha Dasa Babaji, and accepted him as his eternally worshipable siksa (instructing) guru.</p>

<p>From Vrindavana he went to Calcutta and bought a house. He started daily worship of Sri Giridhari (the transcendental form of Krishna who appeared in the form of Govardhana Hill) and called the house Bhakti-bhavan. In 1881, whilst excavating for the construction of the Bhakti bhavana at Rambagan in Calcutta, a Deity of Kurmadeva was unearthed. Bhaktivinoda entrusted his son Bimala with the service of the deity of Kurmadeva. In Calcutta, Srila Bhaktivinoda Thakura started the Sri Vishva-Vaishnava Sabha, dedicated to the preaching of pure bhakti as taught by Lord Chaitanya. To publicize the work of the society, Srila Bhaktivinoda Thakura published a small booklet entitled Vishva-Vaishnava-kalpavi.</p>



<p>Also he published his own edition of the Sri Chaitanya-Charitamrita, with his Amrta-prabhava Bhashya commentary. And he introduced the Chaitanyabda or Chaitanya-era calendar, and gave assistance to the propagation of the Chaitanya Panjika, which established the feast day of Gaura Purnima.</p>

<p>He lectured and gave readings on books such as Bhakti Rasamrita Sindhu (of Srila Rupa Goswami) in various Vaishnava societies; he published in the Hindu Herald, an English periodical, a detailed account of Sri Chaitanya's life. It was at this time that the learned Vaishnavas recognized Kedaranatha Datta and gave him the honorary title of Bhaktivinoda Thakura.</p>

<p>Bhaktivinoda travelled and preached widely. In 1891, he took leave from government service for two years just for this purpose. He would lecture in clubs, societies and organizations. During this time he continued writing and opened many branches of Nama Hatta in different districts of Bengal. The Nama Hatta became a self-sustaining success which continued to spread even after his return to government service.

Once whilst stationed in Krishanaga, Bhaktivinoda Thakura, along with Jagannatha Dasa Babaji Maharaja found Lord Chaitanya's birthplace in the year 1887. When the birthplace was uncovered, Srila Bhaktivinoda Thakura and Srila Jagannatha Dasa Babaji would worship Lord Chaitanya there.</p>

<p>In 1908 Srila Bhaktivinoda Thakura took the external vesha dress of a babaji at Satasana, Puri , i.e. he accepted paramahamsa-sannyasa. Until 1910 he travelled between Calcutta and Puri, still writing books; but during that year he shut himself up and entered samadhi, claiming paralysis.</p>

<p>On June 23rd, 1914, at Jagannatha Puri, Srila Bhaktivinoda Thakura Prabhupada passed away. This day was also the disappearance day of Sri Gadadhara Pandita.</p>

