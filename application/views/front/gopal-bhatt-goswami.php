<p>When Lord Chaitanya heard that Gopal Bhatt was in Vrindavan, He was extremely pleased. The Lord sent some of His personal belongings to Gopal Bhatt, who worshiped them. The Lord also sent a letter instructing Gopal Bhatt to help Rupa and Sanatan compile transcendental literature. Gopal Bhatt accepted this instruction from the Lord as his life and soul, and he later engaged his disciple Shrinivasa Acharya in carrying the writings to Bengal.</p>

<p>Once, on a trip to the Gandaki River, in Nepal, Gopal Bhatt obtained twelve salagrama-silas. (A sila is a special Deity of the Lord in the form of a stone.) The silas entered his water pot as he filled it with water from the river. When he tried to return them to the river and refill his pot, they again entered the pot. Accepting this as the Lord's mercy, Gopal Bhatt decided to bring the silas back to Vrindavan.</p>

<p>One day, Gopal Bhatt felt the need to worship a Deity of Krishna. The next morning he saw that his silas had transformed into a beautiful Deity of Lord Krishna. Gopal Bhatt named the Deity Radha-Raman, ‘Krishna, who brings pleasure to Radharani.’ He established the worship of Radha-Raman, and the Radha-Ramana temple is still one of the main places of pilgrimage in Vrindavan.</p>
