                  <p><b>Shrila Gour Kishor Das Babaji</b> was born in 1838. He spent almost thirty years as a householder looking after his wife and family. After his wife died, he gave up his business as a grain merchant and approached Bhagavat Das Babaji, a disciple of Jagannatha Das Babaji, for Babaji initiation. For the next thirty years Shrila Gour Kishor travelled from village to village in Vrindavan, continually worshiping the Lord. He lived by begging and slept under trees. He would lie prostrate, offering his humble obeisance to the residents of Vraja, considering them as embodiments of Lord Krishna. He even offered his obeisance to the flowers, trees, and land around him.</p>



<p>Srila Gaurakishora later moved to the Navadvipa where he was to spend the rest of his life, moving from village to village. He would consider these villages to be non-different from Vrindavana. He would dress himself in the discarded cloth used to dress corpses, beg rice from householders, and cook with discarded earthen pots. He would dance on the banks of the Ganges chanting the holy names of Krishna; at other times he would lie on the ground unconscious.

Shrila Gour Kishor Das had very few possessions: Tulasi-mala (Tulasi beads) around his neck; another Tulasi-mala on which he chanted; and a few books such as Narottam Das Thakur's Prarthana and Prem Bhakti Chandrika.</p>

<p>Shrila Gour Kishor Das Babaji refused service from anyone and would not let anyone serve him, though many tried. Although illiterate, he was regarded as vastly learned and self-realized. Many people would consult Shrila Gour Kishor, but he was able to detect and reveal sincerity and deceit in people. He showed neither distaste for his enemies nor affection for those dear to him. Shrila Gour Kishor was surrounded by many insincere devotees but never encouraged them nor drove them away.</p>

<p>Shrila Gour Kishor would often visit Svananda Kunja, a place within Godrumadvipa (one of the nine islands of Navadvipa), where he would listen to Shrila Bhaktivinod Thakur talk on Srimad-Bhagavatam and other topics concerning the conclusions of devotional service. Babaji Maharaja would often visit Bhaktivinoda. They would discuss Srimad-Bhagavatam together at Bhaktivinoda Thakura's house. Srila Bhaktivinoda Thakura, noting the faultless behaviour of Shrila Gour Kishor Das Babaji , held him in the utmost regard and admiration.

In 1900 Shrila Gour Kishor Das Babaji accepted Bhaktivinod's son, Bimal Prasad, as his disciple. He gave him the name Varshabhanavi-devi-dayit Das; he also gave him the tiger-skin hat and basket that had been given to Gour Kishor by his spiritual master Bhagavat Dasa Babaji, which he in turn had received from Jagannath das Babaji.</p> 

<p>Varsabhanavi-devi-dayita Dasa later became known as Shrila Bhaktisiddhanta Sarasvati Swami Prabhupaad, and was the founder of the Chaitanya Mathas and Gaudiya Mathas.</p>

<p>In 1908 Shrila Gour Kishor Das Babaji lost his sight. He refused attempts by Varsabhanavi-devi-dayita Dasa to take him to an eye specialist, preferring instead to chant and worship Krishna. He stopped travelling at this point. He would sit in his bhajana-kutir deeply absorbed in Krishna’s pastimes.</p>

<p>On 17 November 1915, Gaurakishora Dasa Babaji Maharaja passed away. His body was placed into a samadhi (tomb) but due to flooding, the samadhi had to be moved. Many Babajis tried to claim the samadhi but were told by Bhaktisiddhanta Sarasvati: 'I am the only disciple of Paramahamsa Babaji Maharaja. Even though I have not accepted sannyasa, I am a celibate brahmachari and by the grace of</p>

<p>Babaji Maharaj I am not secretly addicted to abominable habits or involved in fornication as some monkey-like people are. If there is someone amongst the people here present who is a renunciate of stainless character, then he can have Babaji Maharaja's samadhi. We have no objection to that. He, who within the last year, or the last six months, three months, one month or at least within the last three days, has not had illicit connection with a woman will be able to touch this spiritual blissful body. If anyone else touches it he will be completely ruined.' A police inspector who was present asked, 'How will evidence be had for this?' Srila Bhaktisiddhanta Sarasvati replied: 'I have faith in their word.’ At this, one by one all the bogus Babajis slipped away without a word. Shrila Bhaktisiddhanta Sarasvati then claimed the samadhi.</p>

