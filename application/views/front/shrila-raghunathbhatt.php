                    <p><b>Raghunath Bhatt Goswami</b> was born in 1505 in Eastern Bengal . His father, Shri Tapan Mishra, was a great devotee of Shri Chaitanya Mahaprabhu. Shri Tapan Mishra met Shri Chaitanya Mahaprabhu, first in Eastern Bengal, and then again in Varanasi . While in Varanasi , Shri Chaitanya Mahaprabhu used to dine at Shri Tapan Mishra's home and Raghunath would often massage the Lord's feet. He became very attached to the Lord.</p>



<p>As a boy Raghunath was adept in Sanskrit grammar and rhetoric. He became well versed in the scriptures. Upon coming of age his father sent him to Puri, where he met Shri Chaitanya Mahaprabhu. He spent eight months in the service of the Lord. He would cook for him on a regular basis. Shriman Mahaprabhu instructed Raghunath never to marry and to study the scriptures. He ordered him to return to Varanasi and to care for his ageing parents. He further told him that he would see Him again in Jagannath Puri. Shri Chaitanya Mahaprabhu gave Raghunath a tulasi garland from his neck.</p>



<p>When Raghunath 's parents passed away, he returned to Jagannath Puri where he spent a further eight months in the service of the Lord. Shri Chaitanya Mahaprabhu then sent Raghunath to Vrindavan, to study the Shrimad-Bhagavatam and other revealed scriptures under the guidance of Shri Sanatan and Shri Rupa Goswami. After some time Raghunatha's disciples built a temple for Shri Gaur Govind in Vrindavan.</p>



<p>Raghunath was full of humility and meekness. He was permanently engaged in remembrance of the Lord. He dissappeared in 1579.</p>

