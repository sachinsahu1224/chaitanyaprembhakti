                  <p><b>Shrila Raghunath Das Goswami</b> is an eternally liberated associate of Lord Krishna who appeared to join the Lord when He appeared five hundred years ago as Shri Chaitanya Mahaprabhu.</p>



<p>Although the son of a very rich landlord, Raghunath Das had no interest in the things of this world. His sole desire was to gain the association of Lord Chaitanya and dedicate himself to the Lord’s service. Seeing Raghunath’s spirit of renunciation even as a young man, his family tried to keep him at home by all means, including guards. Somehow Raghunath was able to escape their vigilance, and he made his way to Jagannath Puri to serve Lord Chaitanya.</p>



<p>Raghunath’s life in Puri showed him to be absorbed in transcendental consciousness. In the Chaitanya Charitamrita, Krishnadas Kaviraj Goswami writes: ‘Who could list the unlimited transcendental attributes of Raghunath Das? His strict regulative principles were exactly like the lines on a stone. Raghunath Das spent more than twenty-two hours out of every twenty-four chanting the Hare Krishna maha-mantra and remembering the lotus feet of the Lord. He ate and slept for less than an hour and a half, and on some days that also was impossible.’</p>



<p>Topics concerning his renunciation are wonderful. Throughout his life he never allowed his tongue sense gratification. He never touched anything to wear except a small torn cloth and a patchwork wrapper. Whatever he ate was only to keep his body and soul together, and when he ate he would reproach himself thus:</p>



<p>“If one’s heart has been cleansed by perfect knowledge and one has understood Krishna , the Supreme Brahman, he then gains everything. Why should such a person act like a debauchee by trying very carefully to maintain his material body?”‘</p>



<p>Raghunath Das Goswami served Lord Chaitanya for sixteen years at Jagannath Puri. After the Lord departed this world, Raghunath went to Vrindavan, where he lived for many years at the sacred lake Radha Kunda. His bhajan kutir, or place of worship, still exists there.</p>

